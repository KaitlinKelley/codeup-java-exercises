public class StringExercise {
    public static void main(String[] args){
//        String Basics.
//
//        Create a class named StringExercise with a main method.
//
//        For each of the following output examples: create a String variable that contains the desired output and print it out to the console, you can do this with only one String variable and one print statement for each output example.
//
//
//                We don't need no education
//        We don't need no thought control
//
//        Check "this" out!, "s inside of "s!
//
//                In windows, the main drive is usually C:\
//
//        I can do backslashes \, double backslashes \\,
//        and the amazing triple backslash \\\!

        String pinkFloydLyric = "\t\tWe don\'t need no education \nWe don\'t need no thought control";
        System.out.println(pinkFloydLyric);

        System.out.println();

        String checkThisOut = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkThisOut);

        System.out.println();

        String windows = "\t\tIn windows, the main drive is usually C:\\";
        System.out.println(windows);

        System.out.println();

        String backslashes = "I can do backslashes \\, double backslashes \\\\,\n and the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);
    }
}
